package com.joshuaavalon.wsdeckeditor.sdk.util;

public class Range {
    private int max;
    private int min;

    public Range() {
        max = 0;
        min = 0;
    }

    public int getMax() {
        return max;
    }

    public void setMax(int max) {
        this.max = max;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }
}
